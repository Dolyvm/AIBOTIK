def get_translated_greeting(character_id: str, scenario_index: int, character_data) -> str:
    """
    Получает переведённое приветствие для персонажа и сценария.

    Args:
        character_id: ID персонажа
        scenario_index: Индекс сценария (0 = основной, 1+ = альтернативные)
        character_data: CharacterData объект персонажа

    Returns:
        Текст приветствия на русском языке
    """
    # Сначала проверяем, есть ли перевод
    translations = GREETING_TRANSLATIONS.get(character_id, [])
    if scenario_index < len(translations) and translations[scenario_index]:
        return translations[scenario_index]

    # Fallback на оригинал из character_data
    return character_data.get_greeting(scenario_index)
