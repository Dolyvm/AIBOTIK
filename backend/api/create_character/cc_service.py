def generate_tags(req: CreateCharacterRequest) -> list[str]:
    tags = set()

    if req.style == "anime":
        tags.add("Anime")
    else:
        tags.add("Realistic")

    pers_tags = personality_to_tags.get(req.personality, [])
    tags.update(pers_tags)

    for pref in (req.preferences or []):
        if pref in preference_to_tags:
            tags.add(preference_to_tags[pref])

    tags.add("NSFW")
    tags.add("Adult")

    return list(tags)

def build_personality_with_preferences(req: CreateCharacterRequest) -> str:
    base = personality_to_prompt.get(req.personality, req.personality)

    if req.preferences:
        prefs_text = ", ".join(req.preferences)
        return f"{base} Предпочтения в интимной сфере: {prefs_text}."

    return base

async def build_character_prompt(prompt: str) -> str:
    """Для того, чтобы создать персонажа из текстового описания пользователя. """
    from shared.services.prompt_service import get_prompt

    prompt_template = await get_prompt("create_character_prompt")
    return prompt_template.format(user_prompt=prompt).strip()


async def build_llm_prompt(req: CreateCharacterRequest) -> str:
    from shared.services.prompt_service import get_prompt

    prompt_template = await get_prompt("cc_scenario_prompt")
    return prompt_template.format(
        name=req.name,
        personality=req.personality,
        relationship=req.relationship,
        nationality=req.nationality or "не указана"
    ).strip()

async def build_description_prompt(req: CreateCharacterRequest) -> str:
    from shared.services.prompt_service import get_prompt

    prompt_template = await get_prompt("cc_description_prompt")
    return prompt_template.format(
        name=req.name,
        age=req.age,
        nationality=req.nationality or "не указана",
        personality=req.personality,
        relationship=req.relationship,
        preferences=', '.join(req.preferences) if req.preferences else 'не указаны'
    ).strip()

